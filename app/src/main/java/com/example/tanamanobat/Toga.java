package com.example.tanamanobat;

public class Toga {
    private String toga;
    private String detail_toga;
    private int photo_toga;

    public String getToga() {
        return toga;
    }

    public void setToga(String toga) {
        this.toga = toga;
    }

    public String getDetail_toga() {
        return detail_toga;
    }

    public void setDetail_toga(String detail_toga) {
        this.detail_toga = detail_toga;
    }

    public int getPhoto_toga() {
        return photo_toga;
    }

    public void setPhoto_toga(int photo_toga) {
        this.photo_toga = photo_toga;
    }
}


